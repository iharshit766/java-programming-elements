public class HarryAge {
    public static void main(String[] args) {
        // store birth year
        int birthYear = 2000;  
        
        // store current year
        int currentYear = 2024;  
        
        // calculate age
        int age = currentYear - birthYear;  
        
        // print the result
        System.out.println("Harry's age in 2024 is " + age);  
    }
}

